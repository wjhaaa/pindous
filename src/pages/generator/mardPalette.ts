let _paletteCache: MardColor[] | null = null;

export async function loadMardPalette(): Promise<MardColor[]> {
  if (_paletteCache) return _paletteCache;
  _paletteCache = MARD_HEX.map(({ code, hex }) => ({
    code,
    hex,
    rgb: hexToRgb(hex),
  }));
  return _paletteCache;
}

